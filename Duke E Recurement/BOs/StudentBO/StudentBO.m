#import "StudentBO.h"

@implementation StudentBO

@synthesize studentID,firstName,lastName,check_in,email,sID,partnerKey;

-(void)dealloc{
    
    [firstName release];
    firstName = nil;
    [lastName release];
    lastName = nil;
    [check_in release];
    check_in = nil;
    [email release];
    email = nil;
    [sID release];
    sID = nil;
    [partnerKey release];
    partnerKey = nil;
    
    [super dealloc];
}

@end
